package ingsw.proyecto.SAGI.model;

public class ingresoMes {

	private String mes;
	private int Deuda;
	private int Aportes;

	public String getMes() {
		return mes;
	}

	public void setMes(String mes) {
		this.mes = mes;
	}

	public int getDeuda() {
		return Deuda;
	}

	public void setDeuda(int deuda) {
		Deuda = deuda;
	}

	public int getAportes() {
		return Aportes;
	}

	public void setAportes(int aportes) {
		Aportes = aportes;
	}

}
